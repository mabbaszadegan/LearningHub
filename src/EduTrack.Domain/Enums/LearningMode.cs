namespace EduTrack.Domain.Enums;

/// <summary>
/// Represents the learning mode for a student's enrollment
/// </summary>
public enum LearningMode
{
    /// <summary>
    /// Self-study mode - students work at their own pace
    /// </summary>
    SelfStudy = 0,
    
    /// <summary>
    /// Instructor-guided mode - students follow teacher's schedule and assignments
    /// </summary>
    InstructorGuided = 1
}
